// ********************************************************************************************************************
import { randInt } from "three/src/math/MathUtils";
// ********************************************************************************************************************
import { Vector2 } from "../types/vector2";
// ********************************************************************************************************************
import { DungeonPart, PartType } from "./dungeon-part";
// ********************************************************************************************************************
export class DungeonPartCorridor extends DungeonPart {

    // ****************************************************************************************************************
    // constructor
    // ****************************************************************************************************************
    constructor(public position: Vector2 = new Vector2()) { super(position); }

    // ****************************************************************************************************************
    // function:    getSizeX
    // ****************************************************************************************************************
    // parameters:  n/a
    // ****************************************************************************************************************
    // returns:     the size x
    // ****************************************************************************************************************
    protected override getSizeX(): number {

        return randInt(3, 8);
    }

    // ****************************************************************************************************************
    // function:    getSizeY
    // ****************************************************************************************************************
    // parameters:  n/a
    // ****************************************************************************************************************
    // returns:     the size Y
    // ****************************************************************************************************************
    protected override getSizeY(): number {

        return 1;
    }

    // ****************************************************************************************************************
    // function:    getType
    // ****************************************************************************************************************
    // parameters:  n/a
    // ****************************************************************************************************************
    // returns:     the type
    // ****************************************************************************************************************
    public override getType(): PartType {

        return PartType.CORRIDOR;
    }
}
